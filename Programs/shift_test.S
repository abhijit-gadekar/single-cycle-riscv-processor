.section .text
.globl _start

_start:

    # x1 = 8
    addi x1, x0, 8

    # x2 = 2 (shift amount)
    addi x2, x0, 2

    ################################################
    # Register shifts
    ################################################

    # 8 << 2 = 32
    sll x3, x1, x2

    # 8 >> 2 = 2
    srl x4, x1, x2

    ################################################
    # Immediate shifts
    ################################################

    # 8 << 1 = 16
    slli x5, x1, 1

    # 8 >> 1 = 4
    srli x6, x1, 1

    ################################################
    # Arithmetic right shift
    ################################################

    # x7 = -16
    addi x7, x0, -16

    # -16 >> 2 = -4
    srai x8, x7, 2

    # -16 >> 2 = -4 (register form)
    sra x9, x7, x2

done:
    beq x0, x0, done
